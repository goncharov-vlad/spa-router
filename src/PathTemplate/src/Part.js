/**
 * @param name {string}
 * @param type {string}
 */
export default class {
    /**
     * @param name {string}
     * @param type {string}
     */
    constructor(name, type) {
        this.name = name
        this.type = type

    }

    /**
     * @return {string}
     */
    getName() {
        return this.name

    }

    /**
     * @return {string}
     */
    getType() {
        return this.type

    }

}